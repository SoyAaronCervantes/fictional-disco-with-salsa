  override func viewWillAppear(_ animated: Bool) {
    super.viewWillAppear(animated)
    
    // Get app delegate reference
    let appDelegate = UIApplication.shared.delegate as! AppDelegate
    
    // Get people data from DB
    people = appDelegate.people()
    
    // Refresh table view with people
    tableView.reloadData()
    
  }

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
      return people.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
      let cell = tableView.dequeueReusableCell(withIdentifier: "reuseIdentifier", for: indexPath)

      let person = people[ indexPath.row ]
      let name = ( person.name ?? "" )
      let lastName = (person.last_name ?? "" )
      let middleName = (person.middle_name ?? "" )

      cell.textLabel?.text = name + " " + lastName + " " + middleName
      cell.imageView?.image = UIImage( named: "tel-icon" )

      return cell
    }

